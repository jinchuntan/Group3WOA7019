// A small set of built-in icons that can be attached to a note.
//
// We deliberately keep the list short and use semantic names (rather than
// emoji or asset paths) so the catalogue can map each value to whichever
// rendering style the platform supports best (TMP sprite, glyph, or texture).
public enum NoteIcon
{
    None = 0,
    Note = 1,
    Star = 2,
    Alarm = 3,
    Heart = 4,
    Shopping = 5,
    Work = 6,
    Study = 7,
    Home = 8,
}
